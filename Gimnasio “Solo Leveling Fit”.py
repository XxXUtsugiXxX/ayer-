# 3. Gimnasio “Solo Leveling Fit” – Motivación diaria
# Como entrenador, quiero usar un while que repita 5 veces el mensaje
# “¡Tú puedes lograrlo!”, pero en la última repetición muestre “¡Excelente trabajo,
# terminaste!”.

contador = 0
while contador < 5:
    print("¡Tú puedes lograrlo!")
    contador += 1
else:
    print("Excelente trabajo, terminaste!")